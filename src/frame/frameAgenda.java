    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    try {
        if (txtid.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "ID Pengeluaran Belum diisi");
            txtid.requestFocus();
        } else if (combo.getSelectedItem() == null){
            JOptionPane.showMessageDialog(null, "ID Petugas belum diisi");
            combo.requestFocus();
        } else if (txtjudul.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "judul Belum diisi");
        } else if (txtjamm.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "jam mulai Belum diisi");
        } else if (txtjs.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "jam selesai Belum diisi");
        } else if (txttm.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "tanggal mulai Belum diisi");
        } else if (txtts.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "tanggal selesai Belum diisi");
        }  else if (txtsel.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Keterangan Belum diisi");
        } else {
            // ðŸ”¹ Tambahkan log agar tahu event dijalankan
            System.out.println("Tombol Tambah Ditekan");

            // ðŸ”¹ Panggil fungsi simpan (tidak ubah strukturnya)
            String selectedpetugas = combo.getSelectedItem().toString();
            String[] partspetugas = selectedpetugas.split(" - ", 2); // split ke ID dan nama
            int idPetugas = Integer.parseInt(partspetugas[0].trim());

            agenda.SimpanAnggota(
                Integer.parseInt(txtid.getText()),
                idPetugas,
                txtjudul.getText(),
                txtjamm.getText(),
                txtjs.getText(),                
                txttm.getText(),
                txtts.getText(),
                txtsel.getText()               
            );

            // ðŸ”¹ Tambahkan indikator berhasil
            if (agenda.validasi == true){
                JOptionPane.showMessageDialog(null, "ID sudah terdaftar! Data tidak disimpan.");                
                combo.setSelectedItem(idPetugas + " - " + agenda.VAR_idpetugas);
                txtjudul.setText(agenda.VAR_judul);
                txtjamm.setText(agenda.VAR_jamawal);
                txtjs.setText(agenda.VAR_jamakhir);
                txttm.setText(agenda.VAR_tanggalawal);
                txtts.setText(agenda.VAR_tanggalakhir);                                
                txtsel.setText(agenda.VAR_keterangan);                
            } else {
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan ke database!");
            }
        }
        }  catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
            e.printStackTrace(); // tampilkan detail di console
        }        
       
    }//GEN-LAST:event_jButton1ActionPerformed

