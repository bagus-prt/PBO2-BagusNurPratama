    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    // TODO add your handling code here:
    try {
        if (txtidk.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "ID Petugas Belum diisi");
            txtidk.requestFocus();
        } else if (txtnmkategori.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No Ktp Petugas Belum Diisi");
            txtnmkategori.requestFocus();
        } else {
            // ðŸ”¹ Tambahkan log agar tahu event dijalankan
            System.out.println("Tombol Tambah Ditekan");

            // ðŸ”¹ Panggil fungsi simpan (tidak ubah strukturnya)
            kategori.SimpanAnggota(
                Integer.parseInt(txtidk.getText()),                
                txtnmkategori.getText()
            );

            // ðŸ”¹ Tambahkan indikator berhasil
            if (kategori.validasi == true){
                JOptionPane.showMessageDialog(null, "ID sudah terdaftar! Data tidak disimpan.");
                txtnmkategori.setText(kategori.VAR_namakategori);
            } else {
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan ke database!");
            }
        }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "ID, No KTP, dan No HP harus berupa angka!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
            e.printStackTrace(); // tampilkan detail di console
        }        
    }//GEN-LAST:event_jButton1ActionPerformed

