    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    try {
        if (txtid1.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "ID Pemasukan Belum diisi");
            txtid1.requestFocus();
        } else if (combopetugas.getSelectedItem() == null){
            JOptionPane.showMessageDialog(null, "ID Petugas belum diisi");
            combopetugas.requestFocus();
        } else if (txtnama1.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nama Pemberi Belum diisi");
        } else if (txttgl.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Tanggal Belum diisi");
        } else if (txttotal.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Total Pemasukan Belum diisi");
        } else {
            // ðŸ”¹ Tambahkan log agar tahu event dijalankan
            System.out.println("Tombol Tambah Ditekan");

            // ðŸ”¹ Panggil fungsi simpan (tidak ubah strukturnya)
            String selected = combopetugas.getSelectedItem().toString();
            String[] parts = selected.split(" - ", 2); // split ke ID dan nama
            int idPetugas = Integer.parseInt(parts[0].trim());
            
            pemasukan.SimpanAnggota(
                Integer.parseInt(txtid1.getText()),
                idPetugas,
                txtnama1.getText(),
                txttgl.getText(),                
                txttotal.getText()                
            );

            // ðŸ”¹ Tambahkan indikator berhasil
            if (pemasukan.validasi == true){
                JOptionPane.showMessageDialog(null, "ID sudah terdaftar! Data tidak disimpan.");
                combopetugas.setSelectedItem(idPetugas + " - " + pemasukan.VAR_nama);
                txtnama1.setText(pemasukan.VAR_nama1);
                txttgl.setText(pemasukan.VAR_tanggalpemasukan);                
                txttotal.setText(pemasukan.VAR_totalpemasukan);                
            } else {
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan ke database!");
            }
        }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "ID, No KTP, dan No HP harus berupa angka!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
            e.printStackTrace(); // tampilkan detail di console
        }                            
    }//GEN-LAST:event_jButton1ActionPerformed

