    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    try {
        if (txtid.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "ID Pengeluaran Belum diisi");
            txtid.requestFocus();
        } else if (combopetugas.getSelectedItem() == null){
            JOptionPane.showMessageDialog(null, "ID Petugas belum diisi");
            combopetugas.requestFocus();
        }else if (combokategori.getSelectedItem() == null){
            JOptionPane.showMessageDialog(null, "ID kategori belum diisi");
            combokategori.requestFocus();
        } else if (txttgl.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "tanggal Pengeluaran Belum diisi");
        } else if (txtnominal.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nominal Belum diisi");
        } else if (txtket.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Keterangan Belum diisi");
        } else {
            // ðŸ”¹ Tambahkan log agar tahu event dijalankan
            System.out.println("Tombol Tambah Ditekan");

            // ðŸ”¹ Panggil fungsi simpan (tidak ubah strukturnya)
            String selectedpetugas = combopetugas.getSelectedItem().toString();
            String[] partspetugas = selectedpetugas.split(" - ", 2); // split ke ID dan nama
            int idPetugas = Integer.parseInt(partspetugas[0].trim());
            
            String selectedkategori = combokategori.getSelectedItem().toString();
            String[] partskategori = selectedkategori.split(" - ", 2); // split ke ID dan nama
            int idKategori = Integer.parseInt(partskategori[0].trim());            
            
            pengeluaran.SimpanAnggota(
                Integer.parseInt(txtid.getText()),
                idPetugas,
                idKategori,
                txttgl.getText(),
                txtnominal.getText(),                
                txtket.getText()                
            );

            // ðŸ”¹ Tambahkan indikator berhasil
            if (pengeluaran.validasi == true){
                JOptionPane.showMessageDialog(null, "ID sudah terdaftar! Data tidak disimpan.");
                combopetugas.setSelectedItem(idPetugas + " - " + pengeluaran.VAR_idpetugas);
                combokategori.setSelectedItem(idKategori + " - " + pengeluaran.VAR_idkategori);
                txttgl.setText(pengeluaran.VAR_tglpengeluaran);
                txtnominal.setText(pengeluaran.VAR_nominal);                
                txtket.setText(pengeluaran.VAR_keterangan);                
            } else {
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan ke database!");
            }
        }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "ID, No KTP, dan No HP harus berupa angka!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
            e.printStackTrace(); // tampilkan detail di console
        }        
    }//GEN-LAST:event_jButton1ActionPerformed

