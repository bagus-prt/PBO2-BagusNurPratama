    private void loadComboPetugas() {
        try {
            ResultSet rs = petugas.getDataPetugas();
            while (rs.next()) {
                String item = rs.getInt("id_petugas") + " - " + rs.getString("nama");
                combopetugas.addItem(item);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data petugas: " + e.getMessage());
    }
    }
        
    private void loadComboKategori() {
        try {
            ResultSet rs = kategori.getDataKategori();
            while (rs.next()) {
                String item = rs.getInt("id_kategori") + " - " + rs.getString("nama_kategori");
                combokategori.addItem(item);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data kategori: " + e.getMessage());
        }        
        
    }

