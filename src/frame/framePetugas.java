    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
    try {
        if (txtid.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan ID yang ingin dihapus!");
            return;
        }

        int id = Integer.parseInt(txtid.getText());
        tblpetugas.hapusAnggota01(id);

        // Kosongkan textfield setelah data berhasil dihapus
        txtid.setText(null);
        txtktp.setText(null);
        txtnama.setText(null);
        txtalamat.setText(null);
        txtnohp.setText(null);
        txtusername.setText(null);
        txtpassword.setText(null);
       

        JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "ID harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }        
    }//GEN-LAST:event_btnhapusActionPerformed

    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
    // TODO add your handling code here:
    try {
        if (txtid.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "ID Petugas Belum diisi");
            txtid.requestFocus();
        } else if (txtktp.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No Ktp Petugas Belum Diisi");
            txtktp.requestFocus();
        } else if (txtnama.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nama Petugas Belum diisi");
        } else if (txtalamat.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Alamat Belum diisi");
        } else if (txtnohp.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No Hp Belum diisi");
        } else if (txtusername.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Username Belum diisi");
        } else if (txtpassword.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Password Belum diisi");
        } else {
            // ðŸ”¹ Tambahkan log agar tahu event dijalankan
            System.out.println("Tombol Tambah Ditekan");

            // ðŸ”¹ Panggil fungsi simpan (tidak ubah strukturnya)
            tblpetugas.SimpanAnggota(
                Integer.parseInt(txtid.getText()),
                Integer.parseInt(txtktp.getText()),
                txtnama.getText(),
                txtalamat.getText(),
                Integer.parseInt(txtnohp.getText()),
                txtusername.getText(),
                txtpassword.getText()
            );

            // ðŸ”¹ Tambahkan indikator berhasil
            if (tblpetugas.validasi == true){
                JOptionPane.showMessageDialog(null, "ID sudah terdaftar! Data tidak disimpan.");
                txtktp.setText(String.valueOf(tblpetugas.VAR_noKtp));
                txtnama.setText(tblpetugas.VAR_nama);
                txtalamat.setText(tblpetugas.VAR_alamat);
                txtnohp.setText(String.valueOf(tblpetugas.VAR_noHp));
                txtusername.setText(tblpetugas.VAR_username);
                txtpassword.setText(tblpetugas.VAR_password);
            } else {
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan ke database!");
            }
        }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "ID, No KTP, dan No HP harus berupa angka!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
            e.printStackTrace(); // tampilkan detail di console
        }                
    }//GEN-LAST:event_btntambahActionPerformed

